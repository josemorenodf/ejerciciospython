nombre1="mAriA"
print(nombre1)
nombre2=nombre1.upper() # Devuelve una cadena de caracteres convirtiendo todos sus caracteres a mayúsculas.
print(nombre2)
nombre3=nombre1.lower() # Devuelve una cadena de caracteres convirtiendo todos sus caracteres a minúsculas.
print(nombre3)
nombre4=nombre1.capitalize() # Devuelve una cadena de caracteres convertida a mayúscula solo su primer caracter y todos los demás a minúsculas.
print(nombre4)