import numpy as np

# .shape permite conocer cuantas filas y columnas tiene un array
a = np.array([2,3,4])  # Arrays Unidimensionales: Vectores
b = np.array([[2,5,1],[6,5,3],[9,8,2],[9,8,2]])  # Arrays Multidimensionales o Matrices
print("filas y columnas array a")
print(a.shape)
print(a)
print()
print("filas y columnas array b")
print(b.shape)
print(b)