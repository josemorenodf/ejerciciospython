# Carga por teclado de los elementos
# Utilizar el método append que tiene la lista para pasar como parámetro el nuevo elemento

lista=[10,20,30]
print(f"1) La longitud de la lista es: {len(lista)}")
lista.append(100) # Asignación de un nuevo elemento
print(f"2) La nueva longitud de la lista es: {len(lista)}")
print(lista[0]) # Imprime un 10
print(lista[3]) # Imprime un 100