# Definir una lista por asignación que almacene los nombres de los primeros cuatro meses del año. Mostrar el primer y último elemento de la lista.

meses=["Enero", "Febrero", "Marzo", "Abril"]
print("Longitud de la lista:",len(meses))
print(meses[0]) # Se muestra enero
print("********************")
print(meses[3]) # Se muestra abril
print("********************")
print(meses[len(meses)-2]) # Muestra el penúltimo elemento de la lista - marzo
print("********************")
print(meses[len(meses)-1]) # Muestra el último elemento de la lista - abril