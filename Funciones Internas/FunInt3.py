"""Comandos de salida"""

# exit()
# Si el valor de i es 5, entonces el programa está forzado a salir

for i in range(10):
    if i==5:
        print(exit)
        exit()
    print(i)

# quit()
# Si el valor de i es 5, entonces el programa está forzado a salir

for i in range(10):
    if i==5:
        print(quit)
        quit()
    print(i)